    private void updateClientStatus() {
    Thread updateThread = new Thread(() -> {
        try (ServerSocket serverSocket = new ServerSocket(PORT)) {
            while (true) {
                Socket clientSocket = serverSocket.accept();
                consolearea.append("New client connected...\n");
                BufferedReader reader = new BufferedReader(new InputStreamReader(clientSocket.getInputStream()));
                String username = reader.readLine(); // Đọc tên người dùng từ client
                // Kiểm tra xem người dùng đã tồn tại trong bảng viewClient chưa
                boolean userExists = false;
                for (int i = 0; i < viewClient.getRowCount(); i++) {
                    if (viewClient.getValueAt(i, 0).equals(username)) {
                        userExists = true;
                        break;
                    }
                }
                if (!userExists) { // Nếu người dùng chưa tồn tại, thêm mới vào bảng
                    DefaultTableModel model = (DefaultTableModel) viewClient.getModel();
                    model.addRow(new Object[]{username, "Đang kết nối"}); // Thêm dòng mới vào bảng
                }
                clientSocket.close();
            }
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    });
    updateThread.start();
}
    
